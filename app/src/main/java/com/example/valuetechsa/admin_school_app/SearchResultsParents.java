public class SearchResultsParents {
    private String id;
    private String name;
    private String email;
    private String password;
    private String phone;
    private String address;
    private String profession;
    private String children;


    public String getParentId() {
        return id;
    }

    public void setParentId(String id) {
        this.id = id;
    }

    public String getParentName() {
        return name;
    }

    public void setParentName(String name) {
        this.name = name;
    }

    public String getParentEmail() {
        return email;
    }

    public void setParentEmail(String email) {
        this.email = email;
    }

    public String getParentPassword() {
        return password;
    }

    public void setParentPassword(String password) {
        this.password = password;
    }

    public String getParentPhone() {
        return phone;
    }

    public void setParentPhone(String phone) {
        this.phone = phone;
    }

    public String getParentAddress() {
        return address;
    }

    public void setParentAddress(String address) {
        this.address = address;
    }

    public String getParentProfession() {
        return profession;
    }

    public void setParentProfession(String profession) {
        this.profession = profession;
    }

    public String getParentChildren() {
        return children;
    }

    public void setParentChildren(String children) {
        this.children = children;
    }
}
